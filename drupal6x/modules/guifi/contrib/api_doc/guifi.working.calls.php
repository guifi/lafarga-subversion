<h3 id="working_calls">Crides a l'API</h3>

<p>L'API de guifi.net funciona a través del protocol HTTP.</p>

<p>En concret, actualment l'API només suporta el mètode GET del protocol
HTTP com a entrada de dades.</p>

<p>Per tant, una possible crida a l'API de guifi.net s'efectuaria
mitjançant l'URL:</p>

<blockquote><pre>http://guifi.net/api?command=guifi.misc.protocol</pre></blockquote>
